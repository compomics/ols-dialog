    /**
     * Inserts the selected term into the OLS Dialog.
     *
     * @param evt
     */
    private void okJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okJButtonActionPerformed
        String selectedSpecies = mostCommonSpeciesJComboBox.getSelectedItem().toString();
        String termName = selectedSpecies.substring(0, selectedSpecies.indexOf("[") - 1);
        String termId = selectedSpecies.substring(selectedSpecies.indexOf("[") + 1, selectedSpecies.length() - 1);
        olsDialog.insertNewtSelection(termName, termId);
        closeDialog(null);
    }//GEN-LAST:event_okJButtonActionPerformed
